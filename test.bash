#!/usr/bin/env bash
set -uo pipefail
PASS=0
FAIL=0
BIN="cargo run -q --"

norm() {
  sed -E 's/[0-9]{4}-[0-9]{2}-[0-9]{2}T[0-9]{2}:[0-9]{2}:[0-9]{2}/2020-01-01T00:00:00/g'
}

check() {
  local n="$1" e="$2" a="$3"
  if [ "$a" = "$e" ]; then
    echo "  PASS: $n"
    PASS=$((PASS + 1))
  else
    echo "  FAIL: $n"
    diff <(echo "$e") <(echo "$a") || true
    FAIL=$((FAIL + 1))
  fi
}

$BIN drop

echo "=== add ==="
check "add 1" "Task added" "$($BIN add "Grocery Shopping" -n "Buy milk and eggs" -d 2026-12-31 -g "Personal" -p high)"
check "add 2" "Task added" "$($BIN add "Work Report" -n "Q4 report summary" -d 2026-11-15 -g "Office" -p critical)"
check "add 3" "Task added" "$($BIN add "Sub Task" -n "reminder note" -s 1 -g "Personal" -p low)"

echo "=== list ==="
check "list: 3 tasks" "\
+----+------------------+-------------------+------------+---------+----------+----------+-----------+---------------------+---------------------+
| ID | Title            | Note              | Due        | Subtask | Group    | Priority | Completed | Created             | Updated             |
|----+------------------+-------------------+------------+---------+----------+----------+-----------+---------------------+---------------------|
| 1  | Grocery Shopping | Buy milk and eggs | 2026-12-31 |         | Personal | high     | ✗         | 2020-01-01T00:00:00 | 2020-01-01T00:00:00 |
|----+------------------+-------------------+------------+---------+----------+----------+-----------+---------------------+---------------------|
| 2  | Work Report      | Q4 report summary | 2026-11-15 |         | Office   | critical | ✗         | 2020-01-01T00:00:00 | 2020-01-01T00:00:00 |
|----+------------------+-------------------+------------+---------+----------+----------+-----------+---------------------+---------------------|
| 3  | Sub Task         | reminder note     |            | 1       | Personal | low      | ✗         | 2020-01-01T00:00:00 | 2020-01-01T00:00:00 |
+----+------------------+-------------------+------------+---------+----------+----------+-----------+---------------------+---------------------+" \
  "$($BIN list | norm)"

echo "=== edit ==="
check "edit" "Task updated" "$($BIN edit 1 -t "Grocery List" -n "Buy milk, eggs, bread" -d 2026-12-25 -g "Home" -p medium --completed true)"
check "list: after edit" "\
+----+--------------+-----------------------+------------+---------+----------+----------+-----------+---------------------+---------------------+
| ID | Title        | Note                  | Due        | Subtask | Group    | Priority | Completed | Created             | Updated             |
|----+--------------+-----------------------+------------+---------+----------+----------+-----------+---------------------+---------------------|
| 1  | Grocery List | Buy milk, eggs, bread | 2026-12-25 |         | Home     | medium   | ✓         | 2020-01-01T00:00:00 | 2020-01-01T00:00:00 |
|----+--------------+-----------------------+------------+---------+----------+----------+-----------+---------------------+---------------------|
| 2  | Work Report  | Q4 report summary     | 2026-11-15 |         | Office   | critical | ✗         | 2020-01-01T00:00:00 | 2020-01-01T00:00:00 |
|----+--------------+-----------------------+------------+---------+----------+----------+-----------+---------------------+---------------------|
| 3  | Sub Task     | reminder note         |            | 1       | Personal | low      | ✗         | 2020-01-01T00:00:00 | 2020-01-01T00:00:00 |
+----+--------------+-----------------------+------------+---------+----------+----------+-----------+---------------------+---------------------+" \
  "$($BIN list | norm)"

echo "=== find ==="
check "find --title" "\
+----+--------------+-----------------------+------------+---------+-------+----------+-----------+---------------------+---------------------+
| ID | Title        | Note                  | Due        | Subtask | Group | Priority | Completed | Created             | Updated             |
|----+--------------+-----------------------+------------+---------+-------+----------+-----------+---------------------+---------------------|
| 1  | Grocery List | Buy milk, eggs, bread | 2026-12-25 |         | Home  | medium   | ✓         | 2020-01-01T00:00:00 | 2020-01-01T00:00:00 |
+----+--------------+-----------------------+------------+---------+-------+----------+-----------+---------------------+---------------------+" \
  "$($BIN find --title Grocery | norm)"

check "find --note" "\
+----+--------------+-----------------------+------------+---------+-------+----------+-----------+---------------------+---------------------+
| ID | Title        | Note                  | Due        | Subtask | Group | Priority | Completed | Created             | Updated             |
|----+--------------+-----------------------+------------+---------+-------+----------+-----------+---------------------+---------------------|
| 1  | Grocery List | Buy milk, eggs, bread | 2026-12-25 |         | Home  | medium   | ✓         | 2020-01-01T00:00:00 | 2020-01-01T00:00:00 |
+----+--------------+-----------------------+------------+---------+-------+----------+-----------+---------------------+---------------------+" \
  "$($BIN find --note bread | norm)"
check "find --due" "\
+----+--------------+-----------------------+------------+---------+-------+----------+-----------+---------------------+---------------------+
| ID | Title        | Note                  | Due        | Subtask | Group | Priority | Completed | Created             | Updated             |
|----+--------------+-----------------------+------------+---------+-------+----------+-----------+---------------------+---------------------|
| 1  | Grocery List | Buy milk, eggs, bread | 2026-12-25 |         | Home  | medium   | ✓         | 2020-01-01T00:00:00 | 2020-01-01T00:00:00 |
+----+--------------+-----------------------+------------+---------+-------+----------+-----------+---------------------+---------------------+" \
  "$($BIN find --due 2026-12 | norm)"
check "find --group_name" "\
+----+--------------+-----------------------+------------+---------+-------+----------+-----------+---------------------+---------------------+
| ID | Title        | Note                  | Due        | Subtask | Group | Priority | Completed | Created             | Updated             |
|----+--------------+-----------------------+------------+---------+-------+----------+-----------+---------------------+---------------------|
| 1  | Grocery List | Buy milk, eggs, bread | 2026-12-25 |         | Home  | medium   | ✓         | 2020-01-01T00:00:00 | 2020-01-01T00:00:00 |
+----+--------------+-----------------------+------------+---------+-------+----------+-----------+---------------------+---------------------+" \
  "$($BIN find --group_name Home | norm)"
check "find --priority + completed" "\
+----+--------------+-----------------------+------------+---------+-------+----------+-----------+---------------------+---------------------+
| ID | Title        | Note                  | Due        | Subtask | Group | Priority | Completed | Created             | Updated             |
|----+--------------+-----------------------+------------+---------+-------+----------+-----------+---------------------+---------------------|
| 1  | Grocery List | Buy milk, eggs, bread | 2026-12-25 |         | Home  | medium   | ✓         | 2020-01-01T00:00:00 | 2020-01-01T00:00:00 |
+----+--------------+-----------------------+------------+---------+-------+----------+-----------+---------------------+---------------------+" \
  "$($BIN find --priority medium --completed 1 | norm)"

check "find --id 2" "\
+----+-------------+-------------------+------------+---------+--------+----------+-----------+---------------------+---------------------+
| ID | Title       | Note              | Due        | Subtask | Group  | Priority | Completed | Created             | Updated             |
|----+-------------+-------------------+------------+---------+--------+----------+-----------+---------------------+---------------------|
| 2  | Work Report | Q4 report summary | 2026-11-15 |         | Office | critical | ✗         | 2020-01-01T00:00:00 | 2020-01-01T00:00:00 |
+----+-------------+-------------------+------------+---------+--------+----------+-----------+---------------------+---------------------+" \
  "$($BIN find --id 2 | norm)"

check "find --subtask" "\
+----+----------+---------------+-----+---------+----------+----------+-----------+---------------------+---------------------+
| ID | Title    | Note          | Due | Subtask | Group    | Priority | Completed | Created             | Updated             |
|----+----------+---------------+-----+---------+----------+----------+-----------+---------------------+---------------------|
| 3  | Sub Task | reminder note |     | 1       | Personal | low      | ✗         | 2020-01-01T00:00:00 | 2020-01-01T00:00:00 |
+----+----------+---------------+-----+---------+----------+----------+-----------+---------------------+---------------------+" \
  "$($BIN find --subtask 1 | norm)"

check "find --priority critical" "\
+----+-------------+-------------------+------------+---------+--------+----------+-----------+---------------------+---------------------+
| ID | Title       | Note              | Due        | Subtask | Group  | Priority | Completed | Created             | Updated             |
|----+-------------+-------------------+------------+---------+--------+----------+-----------+---------------------+---------------------|
| 2  | Work Report | Q4 report summary | 2026-11-15 |         | Office | critical | ✗         | 2020-01-01T00:00:00 | 2020-01-01T00:00:00 |
+----+-------------+-------------------+------------+---------+--------+----------+-----------+---------------------+---------------------+" \
  "$($BIN find --priority critical | norm)"
check "find: no args" "empty search" "$($BIN find)"

echo "=== rm ==="
check "rm 3" "Task removed" "$($BIN rm 3)"
check "list: after rm 3" "\
+----+--------------+-----------------------+------------+---------+--------+----------+-----------+---------------------+---------------------+
| ID | Title        | Note                  | Due        | Subtask | Group  | Priority | Completed | Created             | Updated             |
|----+--------------+-----------------------+------------+---------+--------+----------+-----------+---------------------+---------------------|
| 1  | Grocery List | Buy milk, eggs, bread | 2026-12-25 |         | Home   | medium   | ✓         | 2020-01-01T00:00:00 | 2020-01-01T00:00:00 |
|----+--------------+-----------------------+------------+---------+--------+----------+-----------+---------------------+---------------------|
| 2  | Work Report  | Q4 report summary     | 2026-11-15 |         | Office | critical | ✗         | 2020-01-01T00:00:00 | 2020-01-01T00:00:00 |
+----+--------------+-----------------------+------------+---------+--------+----------+-----------+---------------------+---------------------+" \
  "$($BIN list | norm)"

check "rm 2" "Task removed" "$($BIN rm 2)"
check "list: after rm 2" "\
+----+--------------+-----------------------+------------+---------+-------+----------+-----------+---------------------+---------------------+
| ID | Title        | Note                  | Due        | Subtask | Group | Priority | Completed | Created             | Updated             |
|----+--------------+-----------------------+------------+---------+-------+----------+-----------+---------------------+---------------------|
| 1  | Grocery List | Buy milk, eggs, bread | 2026-12-25 |         | Home  | medium   | ✓         | 2020-01-01T00:00:00 | 2020-01-01T00:00:00 |
+----+--------------+-----------------------+------------+---------+-------+----------+-----------+---------------------+---------------------+" \
  "$($BIN list | norm)"

echo "=== drop ==="
check "drop" "Tasks dropped" "$($BIN drop)"

echo ""
echo "$PASS passed, $FAIL failed"
if ((FAIL > 0)); then exit 1; fi
