#!/usr/bin/env bash
set -uo pipefail
PASS=0
FAIL=0
BIN=./target/debug/todo-cli-rs
case "$(uname -s)" in
MINGW* | MSYS*) BIN=$BIN.exe ;;
esac

norm() { sed 's/[0-9][0-9][0-9][0-9]-[0-9][0-9]-[0-9][0-9]T[0-9][0-9]:[0-9][0-9]:[0-9][0-9]/2020-01-01T00:00:00/g'; }

check() {
  local n="$1" e="$2" a="$3"
  if [ "$a" = "$e" ]; then
    echo "  PASS: $n"
    PASS=$((PASS + 1))
  else
    echo "  FAIL: $n"
    diff <(echo "$e") <(echo "$a") || true
    FAIL=$((FAIL + 1))
  fi
}

cargo build
$BIN drop 2>&1

echo "=== add ==="
check "add 1" "Task added" "$($BIN add "Grocery Shopping" -n "Buy milk and eggs" -d 2026-12-31 -g "Personal" -p high 2>&1)"
check "add 2" "Task added" "$($BIN add "Work Report" -n "Q4 report summary" -d 2026-11-15 -g "Office" -p critical 2>&1)"
check "add 3" "Task added" "$($BIN add "Sub Task" -n "reminder note" -s 1 -g "Personal" -p low 2>&1)"

echo "=== list ==="
check "list: 3 tasks" "\
+----+------------------+-------------------+------------+---------+----------+----------+-----------+---------------------+---------------------+
| ID | Title            | Note              | Due        | Subtask | Group    | Priority | Completed | Created             | Updated             |
|----+------------------+-------------------+------------+---------+----------+----------+-----------+---------------------+---------------------|
| 1  | Grocery Shopping | Buy milk and eggs | 2026-12-31 |         | Personal | high     | ✗         | 2020-01-01T00:00:00 | 2020-01-01T00:00:00 |
|----+------------------+-------------------+------------+---------+----------+----------+-----------+---------------------+---------------------|
| 2  | Work Report      | Q4 report summary | 2026-11-15 |         | Office   | critical | ✗         | 2020-01-01T00:00:00 | 2020-01-01T00:00:00 |
|----+------------------+-------------------+------------+---------+----------+----------+-----------+---------------------+---------------------|
| 3  | Sub Task         | reminder note     |            | 1       | Personal | low      | ✗         | 2020-01-01T00:00:00 | 2020-01-01T00:00:00 |
+----+------------------+-------------------+------------+---------+----------+----------+-----------+---------------------+---------------------+" \
  "$($BIN list 2>&1 | norm)"

echo "=== edit ==="
check "edit" "Task updated" "$($BIN edit 1 -t "Grocery List" -n "Buy milk, eggs, bread" -d 2026-12-25 -g "Home" -p medium --completed true 2>&1)"
check "list: after edit" "\
+----+--------------+-----------------------+------------+---------+----------+----------+-----------+---------------------+---------------------+
| ID | Title        | Note                  | Due        | Subtask | Group    | Priority | Completed | Created             | Updated             |
|----+--------------+-----------------------+------------+---------+----------+----------+-----------+---------------------+---------------------|
| 1  | Grocery List | Buy milk, eggs, bread | 2026-12-25 |         | Home     | medium   | ✓         | 2020-01-01T00:00:00 | 2020-01-01T00:00:00 |
|----+--------------+-----------------------+------------+---------+----------+----------+-----------+---------------------+---------------------|
| 2  | Work Report  | Q4 report summary     | 2026-11-15 |         | Office   | critical | ✗         | 2020-01-01T00:00:00 | 2020-01-01T00:00:00 |
|----+--------------+-----------------------+------------+---------+----------+----------+-----------+---------------------+---------------------|
| 3  | Sub Task     | reminder note         |            | 1       | Personal | low      | ✗         | 2020-01-01T00:00:00 | 2020-01-01T00:00:00 |
+----+--------------+-----------------------+------------+---------+----------+----------+-----------+---------------------+---------------------+" \
  "$($BIN list 2>&1 | norm)"

echo "=== find ==="
check "find --title" "\
+----+--------------+-----------------------+------------+---------+-------+----------+-----------+---------------------+---------------------+
| ID | Title        | Note                  | Due        | Subtask | Group | Priority | Completed | Created             | Updated             |
|----+--------------+-----------------------+------------+---------+-------+----------+-----------+---------------------+---------------------|
| 1  | Grocery List | Buy milk, eggs, bread | 2026-12-25 |         | Home  | medium   | ✓         | 2020-01-01T00:00:00 | 2020-01-01T00:00:00 |
+----+--------------+-----------------------+------------+---------+-------+----------+-----------+---------------------+---------------------+" \
  "$($BIN find --title Grocery 2>&1 | norm)"

F1=$($BIN find --title Grocery 2>&1 | norm)
check "find --note" "$F1" "$($BIN find --note bread 2>&1 | norm)"
check "find --due" "$F1" "$($BIN find --due 2026-12 2>&1 | norm)"
check "find --group_name" "$F1" "$($BIN find --group_name Home 2>&1 | norm)"
check "find --priority + completed" "$F1" "$($BIN find --priority medium --completed 1 2>&1 | norm)"

check "find --id 2" "\
+----+-------------+-------------------+------------+---------+--------+----------+-----------+---------------------+---------------------+
| ID | Title       | Note              | Due        | Subtask | Group  | Priority | Completed | Created             | Updated             |
|----+-------------+-------------------+------------+---------+--------+----------+-----------+---------------------+---------------------|
| 2  | Work Report | Q4 report summary | 2026-11-15 |         | Office | critical | ✗         | 2020-01-01T00:00:00 | 2020-01-01T00:00:00 |
+----+-------------+-------------------+------------+---------+--------+----------+-----------+---------------------+---------------------+" \
  "$($BIN find --id 2 2>&1 | norm)"

check "find --subtask" "\
+----+----------+---------------+-----+---------+----------+----------+-----------+---------------------+---------------------+
| ID | Title    | Note          | Due | Subtask | Group    | Priority | Completed | Created             | Updated             |
|----+----------+---------------+-----+---------+----------+----------+-----------+---------------------+---------------------|
| 3  | Sub Task | reminder note |     | 1       | Personal | low      | ✗         | 2020-01-01T00:00:00 | 2020-01-01T00:00:00 |
+----+----------+---------------+-----+---------+----------+----------+-----------+---------------------+---------------------+" \
  "$($BIN find --subtask 1 2>&1 | norm)"

F2=$($BIN find --id 2 2>&1 | norm)
check "find --priority critical" "$F2" "$($BIN find --priority critical 2>&1 | norm)"
check "find: no args" "empty search" "$($BIN find 2>&1)"

echo "=== rm ==="
check "rm 3" "Task removed" "$($BIN rm 3 2>&1)"
check "list: after rm 3" "\
+----+--------------+-----------------------+------------+---------+--------+----------+-----------+---------------------+---------------------+
| ID | Title        | Note                  | Due        | Subtask | Group  | Priority | Completed | Created             | Updated             |
|----+--------------+-----------------------+------------+---------+--------+----------+-----------+---------------------+---------------------|
| 1  | Grocery List | Buy milk, eggs, bread | 2026-12-25 |         | Home   | medium   | ✓         | 2020-01-01T00:00:00 | 2020-01-01T00:00:00 |
|----+--------------+-----------------------+------------+---------+--------+----------+-----------+---------------------+---------------------|
| 2  | Work Report  | Q4 report summary     | 2026-11-15 |         | Office | critical | ✗         | 2020-01-01T00:00:00 | 2020-01-01T00:00:00 |
+----+--------------+-----------------------+------------+---------+--------+----------+-----------+---------------------+---------------------+" \
  "$($BIN list 2>&1 | norm)"

check "rm 2" "Task removed" "$($BIN rm 2 2>&1)"
check "list: after rm 2" "\
+----+--------------+-----------------------+------------+---------+-------+----------+-----------+---------------------+---------------------+
| ID | Title        | Note                  | Due        | Subtask | Group | Priority | Completed | Created             | Updated             |
|----+--------------+-----------------------+------------+---------+-------+----------+-----------+---------------------+---------------------|
| 1  | Grocery List | Buy milk, eggs, bread | 2026-12-25 |         | Home  | medium   | ✓         | 2020-01-01T00:00:00 | 2020-01-01T00:00:00 |
+----+--------------+-----------------------+------------+---------+-------+----------+-----------+---------------------+---------------------+" \
  "$($BIN list 2>&1 | norm)"

echo "=== drop ==="
check "drop" "Tasks dropped" "$($BIN drop 2>&1)"

echo ""
echo "$PASS passed, $FAIL failed"
((FAIL > 0)) && exit 1
